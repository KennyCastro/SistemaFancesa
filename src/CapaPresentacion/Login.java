    private void cajaUsuarioMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cajaUsuarioMousePressed
        if(cajaUsuario.getText().equals("Ingresar nombre de usuario")){
            cajaUsuario.setText("");
            cajaUsuario.setForeground(Color.BLACK);
        }
        if(String.copyValueOf(cajaContrasena.getPassword()).isEmpty()){
            cajaContrasena.setText("*********");
            cajaContrasena.setForeground(new Color(204,204,204));
        }
    }//GEN-LAST:event_cajaUsuarioMousePressed

    private void cajaContrasenaMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cajaContrasenaMousePressed
        if(String.copyValueOf(cajaContrasena.getPassword()).equals("*********")){
            cajaContrasena.setText("");
            cajaContrasena.setForeground(Color.BLACK);
        }
        if(cajaUsuario.getText().isEmpty()){
            cajaUsuario.setText("Ingresar nombre de usuario");
            cajaUsuario.setForeground(new Color(204, 204, 204));
            
        }
    }//GEN-LAST:event_cajaContrasenaMousePressed

    private void txtIniciarSesionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_txtIniciarSesionMouseClicked
        datosIniciarSesion datos = new datosIniciarSesion();
           conexionUsuarios conUser = new conexionUsuarios();
           
           if(!((cajaUsuario.getText().equals("") || cajaUsuario.getText().equals("Ingresar nombre de usuario")) || (String.copyValueOf(cajaContrasena.getPassword()).isEmpty() || String.copyValueOf(cajaContrasena.getPassword()).equals("*********")))){
           
                datos.setNameUser(cajaUsuario.getText());
                datos.setPassUser(String.copyValueOf(cajaContrasena.getPassword()));
                if(conUser.IniciarSesionLogin(datos)){
                    
                    Principal pagina = new Principal();
                    pagina.setVisible(true);
                    dispose();
                } else {
                    limpiarCampos();
                }
                
         }
    }//GEN-LAST:event_txtIniciarSesionMouseClicked

