    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(cajaNombre.getText().length()==0) {//esta vacio
            JOptionPane.showConfirmDialog(null, "Ingrese nombre de la persona","VENTANA DE INFORMACIÓN", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
            cajaNombre.requestFocus();
            return;
        }
        if(!expresiones.validarNombres(cajaNombre.getText())){
            JOptionPane.showConfirmDialog(null, "Ingrese un nombre correcto","VENTANA DE INFORMACIÓN", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
            cajaNombre.requestFocus();
            return;
        } 
        
        
         if(cajaApP.getText().length()==0) {//esta vacio
            JOptionPane.showConfirmDialog(null, "Ingrese apellido paterno de la persona","VENTANA DE INFORMACIÓN", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
            cajaApP.requestFocus();
            return;
        }
         
         if(!expresiones.validarNombres(cajaApP.getText())){
            JOptionPane.showConfirmDialog(null, "Ingrese un apellido paterno correcto","VENTANA DE INFORMACIÓN", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
            cajaApP.requestFocus();
            return;
        } 
         
          if(cajaApM.getText().length()==0) {//esta vacio
            JOptionPane.showConfirmDialog(null, "Ingrese apellido materno de la persona","VENTANA DE INFORMACIÓN", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
            cajaApM.requestFocus();
            return;
        }
          
           if(!expresiones.validarNombres(cajaApM.getText())){
            JOptionPane.showConfirmDialog(null, "Ingrese un apellido materno correcto","VENTANA DE INFORMACIÓN", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
            cajaApM.requestFocus();
            return;
        } 
          
           
           if(cajaCi.getText().length()==0) {//esta vacio
            JOptionPane.showConfirmDialog(null, "Ingrese CI de la persona","VENTANA DE INFORMACIÓN", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
            cajaCi.requestFocus();
            return;
        }
           
           if(!expresiones.validarCarnet(cajaCi.getText())){
            JOptionPane.showConfirmDialog(null, "Ingrese un carnet de identidad correcto","VENTANA DE INFORMACIÓN", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
            cajaCi.requestFocus();
            return;
        } 
           
           //el telefono puede estar vacio
           
           if(cajaTelefono.getText().length()!=0){
               if(!expresiones.validarTelefono(cajaTelefono.getText())){
                JOptionPane.showConfirmDialog(null, "Ingrese un teléfono correcto","VENTANA DE INFORMACIÓN", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
                cajaTelefono.requestFocus();
                return;
               }
           }
           
            if(cajaDireccion.getText().length()==0) {//esta vacio
            JOptionPane.showConfirmDialog(null, "Ingrese dirección de la persona","VENTANA DE INFORMACIÓN", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
            cajaDireccion.requestFocus();
            return;
        }
            
            if(!expresiones.validarDireccion(cajaDireccion.getText())){
                JOptionPane.showConfirmDialog(null, "Ingrese una dirección correcta","VENTANA DE INFORMACIÓN", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
                cajaDireccion.requestFocus();
                return;
               }
            
            
            
            
             if((Integer)cajaEdad.getValue()<10 ||  (Integer)cajaEdad.getValue()>100 ) {//esta vacio
            JOptionPane.showConfirmDialog(null, "La edad debe ser mayor a 10 y menor a 100","VENTANA DE INFORMACIÓN", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
            cajaEdad.requestFocus();
            return;
        }
             //del cargo no preguntamos porque si o si estará ya uno escogido por defecto, asi que todo bien ahi
             
              if(cajaSueldo.getText().length()==0 ) {//esta vacio
            JOptionPane.showConfirmDialog(null, "Ingrese sueldo de la persona","VENTANA DE INFORMACIÓN", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
            cajaSueldo.requestFocus();
            return;     
        }
             
              if(!expresiones.validarSueldo(cajaSueldo.getText())){
                JOptionPane.showConfirmDialog(null, "Ingrese un sueldo correcto","VENTANA DE INFORMACIÓN", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
                cajaSueldo.requestFocus();
                return;
               }
              
              
              
              
              datosPersonalCocina datos = new datosPersonalCocina();
              conexionPersonalCocina fun = new conexionPersonalCocina();
              
              datos.setIdPersona(cajaIdPersonal.getText());
              datos.setNombre(cajaNombre.getText());
              datos.setApellidoP(cajaApP.getText());
              datos.setApellidoM(cajaApM.getText());
              datos.setCi(cajaCi.getText());
              datos.setTelefono(cajaTelefono.getText());
              datos.setDireccion(cajaDireccion.getText());
              datos.setEdad((int)cajaEdad.getValue());
              datos.setSueldo(Float.parseFloat(cajaSueldo.getText()));
              datos.setCargo(cajaCargo.getSelectedItem().toString()); 
              //para obtener el valor del radio button
                            Enumeration<AbstractButton> btnGrp = GrupoEstadoPersonalCocina.getElements();
                            String resultado = "";
                            while(btnGrp.hasMoreElements()){
                                JRadioButton btnAux = (JRadioButton)btnGrp.nextElement();
                                if(btnAux.isSelected()){
                                    resultado = btnAux.getText();
                                    break;
                                }
                            }
                            String res="A";
                            if(resultado=="Inactivo"){
                                res="X";
                            }
              datos.setEstado(res);
              datos.setFechaContratacion(cajaFecha.getText());
        
              datos.setNombreImagen(null);//esto debemos eliminarlo
              
              
              if(rutaImage!=""){
                  datos.setFoto(getImage(rutaImage));
              } else {
                  datos.setFoto(null);
              }
              
              
              
              if (accion == "crear"){
                  if(fun.insertarDatos(datos)){
                      JOptionPane.showConfirmDialog(null, "Persona registrada","VENTANA DE INFORMACIÓN", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
                      accion="guardar";
                  }
              } else {
                  if(fun.editarDatos(datos)){
                      JOptionPane.showConfirmDialog(null, "Persona editada","VENTANA DE INFORMACIÓN", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
                  }
                   
              }
              
                    users= new Ventana_Usuarios();
                    setForm(users);
                    
                    cocina = new Ventana_Personal_Cocina();
                    setForm(cocina);
                    
                    
                    
              
             
    }//GEN-LAST:event_btnGuardarActionPerformed

    //esto es para guardar la imagen como bytea en postgres
    
    private byte [] getImage(String ruta){
        File imagen = new File (ruta);
        try {
            byte [] icono = new byte[(int) imagen.length()];
            InputStream input = new FileInputStream(imagen);
            input.read(icono);
            return icono;
        } catch(Exception e ){
            return null;
         }
    }
    
    
    
    
    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        datosPersonalCocina datos = new datosPersonalCocina();
        conexionPersonalCocina fun = new conexionPersonalCocina();
        if (fun.verificarRegistroExistente(cajaIdPersonal.getText())==1){
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Desea eliminar a la persona?","Confirmar",2);
            if(confirmacion==0){
                    datos.setIdPersona(cajaIdPersonal.getText());
                    fun.eliminarDatos(datos);
                    JOptionPane.showConfirmDialog(null, "Persona eliminada","VENTANA DE INFORMACIÓN", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
                    dispose();
                    users= new Ventana_Usuarios();
                    setForm(users);
                    cocina = new Ventana_Personal_Cocina();
                    setForm(cocina);
                    
                    
                    
                    
                    
                }
        }
        else {
             
             JOptionPane.showConfirmDialog(null, "No existe la persona","VENTANA DE INFORMACIÓN", JOptionPane.CLOSED_OPTION, JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnSubirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubirActionPerformed
       //esto es para que al escoger el archivo se vea como el sistema de windows
        try {
            UIManager.setLookAndFeel(UIManager.getSystemLookAndFeelClassName());
        } catch (ClassNotFoundException | InstantiationException | IllegalAccessException | UnsupportedLookAndFeelException ex) {
            Logger.getLogger(Ventana_PersonalCocina_Registro.class.getName()).log(Level.SEVERE, null, ex);
        }
               
        
        JFileChooser filechooser = new JFileChooser();
        FileNameExtensionFilter filtradoExtension = new FileNameExtensionFilter("JPG, PNG", "jpg", "png");
        filechooser.setFileFilter(filtradoExtension);
        
        if (filechooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            rutaImage = filechooser.getSelectedFile().getAbsolutePath();
            Image imagen = new ImageIcon(rutaImage).getImage(); //obtenemos la imagen de la ruta
            ImageIcon mIcono = new ImageIcon(imagen.getScaledInstance(cajaFoto.getWidth(), cajaFoto.getHeight(), 0)); //ajistamos la imagen a la caj donde se almacenara
            cajaFoto.setIcon(mIcono); //mostramos la imagen en la caja
            
        }
        
    }//GEN-LAST:event_btnSubirActionPerformed

