    private void iniciarId(){
        if (idBuscar =="") {
            generarID();
        } else {
            conexionPersonalCocina fun = new conexionPersonalCocina();
            datosPersonalCocina registro = fun.devolverDatos(idBuscar);
            if(registro!= null) {
                IdPersonaBuscar=idBuscar;
                cajaIdPersonal.setText(registro.getIdPersona());
                cajaNombre.setText(registro.getNombre());
                cajaApP.setText(registro.getApellidoP());
                cajaApM.setText(registro.getApellidoM());
                cajaCi.setText(registro.getCi());
                cajaTelefono.setText(registro.getTelefono());
                cajaDireccion.setText(registro.getDireccion());
                cajaEdad.setValue(registro.getEdad());
                cajaSueldo.setText(String.valueOf(registro.getSueldo()));
                cajaCargo.setSelectedItem(registro.getCargo());
                if(registro.getEstado().equals("X")){
                    jrbtnInactivo.setSelected(true);
                }
                cajaFecha.setText(registro.getFechaContratacion());
                
                try{
                    byte[] imagen = registro.getFoto();
                    BufferedImage bufferImage = null;
                    InputStream inputstream = new ByteArrayInputStream(imagen);
                    bufferImage = ImageIO.read(inputstream);
                    ImageIcon nIcono = new ImageIcon(bufferImage.getScaledInstance(172, 150, 0));
                    cajaFoto.setIcon(nIcono);
                    rutaImage="";
                } catch(Exception e){
                    sinFotoDePerfil();
                }
            }
        }
        idBuscar="";
    
    }
